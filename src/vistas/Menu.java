    private void JMenuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JMenuMouseClicked
        TreePath tp = JMenu.getPathForLocation(evt.getX(), evt.getY());
        if (tp != null) {
            String[] aux = tp.toString().split(",")[1].split("-");
            String auxFin = aux[0].trim();
            switch (auxFin) {
                case "00":
                    this.escritorio.removeAll();
                    RegistrarUsuariosF opt1 = new RegistrarUsuariosF();
                    this.escritorio.add(opt1);
                    opt1.show();
                    this.escritorio.repaint();
                    break;
                case "01":
                    this.escritorio.removeAll();
                    GestionarUsuariosF opt2 = new GestionarUsuariosF();
                    this.escritorio.add(opt2);
                    opt2.show();
                    this.escritorio.repaint();
                    break;
                case "03":
                    this.escritorio.removeAll();
                    TareasRegistroF opt4 = new TareasRegistroF();
                    this.escritorio.add(opt4);
                    opt4.show();
                    this.escritorio.repaint();
                    break;
                case "04":
                    this.escritorio.removeAll();
                    DisenadorProcesosF opt5 = new DisenadorProcesosF();
                    this.escritorio.add(opt5);
                    opt5.show();
                    this.escritorio.repaint();
                    break;
                case "05":
                    this.escritorio.removeAll();
                    RegistrarClientesF opt6 = new RegistrarClientesF();
                    this.escritorio.add(opt6);
                    opt6.show();
                    this.escritorio.repaint();
                    break;
                case "07":
                    this.escritorio.removeAll();
                    TareasRegistroF opt8 = new TareasRegistroF();
                    this.escritorio.add(opt8);
                    opt8.show();
                    this.escritorio.repaint();
                    break;
                 case "08":
                    this.escritorio.removeAll();
                    PanelControl opt9 = new PanelControl();
                    this.escritorio.add(opt9);
                    opt9.show();
                    this.escritorio.repaint();
                    break;
            }
        }
    }//GEN-LAST:event_JMenuMouseClicked

    
